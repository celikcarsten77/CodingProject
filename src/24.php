<?php
// This is a simple PHP script to demonstrate basic usage of variables and if statements.
// The variable $userAge is set to 30,
// which represents the age of the user. If the user's age is less than or equal to 18,
// then the message "Welcome" will be displayed, otherwise "Please wait." will be shown.

$userAge = 30; // Set a variable with an initial value
if ($userAge <= 18) {
    echo "Welcome";
} else {
    echo "Please wait.";
}
?>
